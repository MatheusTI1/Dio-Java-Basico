public class Aluno {
    private String nome;
    private int idade;
    private String Sexo;

    public void setSexo(String sexo) {
        Sexo = sexo;
    }
    public String getSexo() {
        return Sexo;
    }

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public int getIdade() {
        return idade;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    }

    
}
